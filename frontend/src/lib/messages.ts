export const messages: Record<Locale, Catalog> = { en, th };

/** Resolve a message with a fallback chain (locale → en → key). String
 * messages support `{name}` interpolation; function messages receive params
 * directly (for pluralization / dates). Never throws, never returns blank. */
export function translate(locale: Locale, key: string, params: MsgParams = {}): string {
	const msg = messages[locale]?.[key] ?? messages.en[key] ?? key;
	if (typeof msg === 'function') return msg(params);
	return msg.replace(/\{(\w+)\}/g, (_, k) => {
		const v = params[k];
		return v === undefined ? `{${k}}` : String(v);
	});
}
